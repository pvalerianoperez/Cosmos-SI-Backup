            //crea la respuesta
            RespuestaBase respuesta = new RespuestaBase();
            
            //listado de respuesta
            List<Cosmos.Entidades.Compras.Personal> listado = new List<Cosmos.Entidades.Compras.Personal>();

            //resultado del metodo 
            Cosmos.Entidades.Compras.Personal resultado = new Cosmos.Entidades.Compras.Personal();
            //intenta llamar al servicio
            try
            {
                string url = string.Format("{0}/api/Compras/Personal/Guardar", AppGlobals.ApiURL);
                respuesta = Funciones.GetAPI(url, solicitud);
                if (respuesta.Exitoso)
                {
                    listado = JsonConvert.DeserializeObject<List<Cosmos.Entidades.Compras.Personal>>(JsonConvert.SerializeObject(respuesta.Datos));
                    if(listado!=null){
                        if(listado.Count>0){
                            resultado = listado[0];
