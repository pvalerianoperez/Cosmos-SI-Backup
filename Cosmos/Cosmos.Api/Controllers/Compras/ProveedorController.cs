        [HttpPost]
        [Route("api/Compras/Proveedor/ConsultarCompleto")]
        public RespuestaBase ConsultarCompleto(SolicitudBase solicitud)
        {

            //crea la respuesta
            RespuestaBase respuesta = new RespuestaBase();

            try
            {
                if (Funciones.AccesoValido(solicitud))
                {

                    Cosmos.Compras.Entidades.Proveedor oP = JsonConvert.DeserializeObject<Cosmos.Compras.Entidades.Proveedor>(JsonConvert.SerializeObject(solicitud.Datos));

                    //establece la conexion
                    SQLHelper.ConnectionString = Properties.Settings.Default.Sistema;

                    //crea la lista vacia
                    List<Cosmos.Compras.Entidades.Proveedor> lst = new List<Cosmos.Compras.Entidades.Proveedor>();

                    //consulta la base de datos
                    lst.Add( Cosmos.Compras.Negocio.Proveedor.ConsultarCompleto(oP.ProveedorID));

                    respuesta.CodigoRespuesta = 1;
                    respuesta.MensajeRespuesta = "OK";
                    respuesta.CodigoError = 0;
                    respuesta.MensajeError = "";
                    respuesta.Exitoso = true;
                    respuesta.Datos = lst;
                }
                else
                {
                    throw new Exception("Acceso inválido.");
                }
            }
            catch (Exception ex)
            {

                respuesta.CodigoRespuesta = 0;
                respuesta.MensajeRespuesta = "";
                respuesta.CodigoError = 1;
                respuesta.MensajeError = ex.Message;
                respuesta.Exitoso = false;
                respuesta.Datos = null;
            }
            //regresa la respuesta
            return respuesta;
        }
